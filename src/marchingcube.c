#include "goxel.h"

static const int N = BLOCK_SIZE;

// Marching cube data.
static const int MC_EDGE_TABLE[256];
static const int8_t MC_TRI_TABLE[256][16];

// Represent a marching cube triangle vertex, by two vertex index and a factor
// between them.  If mu = 0, the vertex is at v0, if mu = 1, the vertex is
// at v1, otherwise the vertex is somewhere in between.
typedef struct {
    int     v0, v1;
    float   mu;
} mc_vert_t;

static int mc_compute(const int neighboors[8],
                      mc_vert_t (*out)[3])
{
    int edges, i, nb_tri;
    int f0, f1;
    int cube_index = 0;
    mc_vert_t verts[12];

    for (i = 0; i < 8; i++) if (neighboors[i] >= 127) cube_index |= 1 << i;
    edges = MC_EDGE_TABLE[cube_index];
    if (!edges) return 0;
    for (i = 0; i < 12; i++) {
        if (!(edges & (1 << i))) continue;
        verts[i].v0 = EDGES_VERTICES[i][0];
        verts[i].v1 = EDGES_VERTICES[i][1];
        f0 = neighboors[verts[i].v0];
        f1 = neighboors[verts[i].v1];
        verts[i].mu = (f0 - 127) / (float)(f0 - f1);
    }
    nb_tri = 0;
    for (i = 0; MC_TRI_TABLE[cube_index][i] != -1; i += 3) {
        out[nb_tri][0] = verts[MC_TRI_TABLE[cube_index][i + 0]];
        out[nb_tri][1] = verts[MC_TRI_TABLE[cube_index][i + 1]];
        out[nb_tri][2] = verts[MC_TRI_TABLE[cube_index][i + 2]];
        nb_tri++;
    }
    assert(nb_tri <= 5);
    return nb_tri;
}

static void mc_interp_pos(const mc_vert_t *vert, int8_t out[3])
{
    int i;
    const int *p0 = VERTICES_POSITIONS[vert->v0];
    const int *p1 = VERTICES_POSITIONS[vert->v1];
    const float mu = vert->mu;
    for (i = 0; i < 3; i++)
        out[i] = (p0[i] * (1 - mu) + p1[i] * mu) * MC_VOXEL_SUB_POS;
}

static vec3_t mc_interp_normal(const mc_vert_t *vert, int normals[8][3])
{
    int i;
    vec3_t ret = vec3_zero;
    const int *p0 = normals[vert->v0];
    const int *p1 = normals[vert->v1];
    const float mu = vert->mu;
    for (i = 0; i < 3; i++)
        ret.v[i] = (p0[i] * (1 - mu) + p1[i] * mu);
    vec3_normalize(&ret);
    return ret;
}

int mesh_generate_vertices_mc(const mesh_t *mesh, const block_t *block,
                              const int block_pos[3], int effects,
                              voxel_vertex_t *out)
{
    int i, vi, x, y, z, v, w, vx, vy, vz, wx, wy, wz, nb_tri, nb_tri_tot = 0;
    int a, sum_a;
    vec3_t n;
    int pos[3];
    uint8_t color[4];
    int colorbest;
    bool use_max_color;
    mesh_iterator_t iter = {0};

    int densities[8];
    int normals[8][3];
    int d, k = 2;

    mc_vert_t tri[5][3];
    if (!(effects & EFFECT_FLAT)) k = 8;

    // Add up the contribution of each voxel to the vertices values.
    for (z = 0; z < N; z++)
    for (y = 0; y < N; y++)
    for (x = 0; x < N; x++) {
        pos[0] = x + block_pos[0];
        pos[1] = y + block_pos[1];
        pos[2] = z + block_pos[2];
        memset(densities, 0, sizeof(densities));
        memset(normals, 0, sizeof(normals));
        n = vec3_zero;
        mesh_get_at(mesh, pos, &iter, color);
        use_max_color = (color[3] == 0);
        colorbest = 8;
        sum_a = 0;
        for (v = 0; v < 8; v++) {

            vx = x + VERTICES_POSITIONS[v][0];
            vy = y + VERTICES_POSITIONS[v][1];
            vz = z + VERTICES_POSITIONS[v][2];

            for (w = 0; w < 8; w++) {
                wx = vx + VERTICES_POSITIONS[w][0] - 1;
                wy = vy + VERTICES_POSITIONS[w][1] - 1;
                wz = vz + VERTICES_POSITIONS[w][2] - 1;
                pos[0] = wx + block_pos[0];
                pos[1] = wy + block_pos[1];
                pos[2] = wz + block_pos[2];
                a = mesh_get_alpha_at(mesh, pos, &iter);

                if (use_max_color && a) {
                    d = abs(x - wx) + abs(y - wy) + abs(z - wz);
                    if (d < colorbest) {
                        mesh_get_at(mesh, pos, &iter, color);
                        colorbest = d;
                    }
                }

                sum_a += a;
                densities[v] += a;

                if (!(effects & EFFECT_FLAT)) {
                    normals[v][0] -= a * (2 * VERTICES_POSITIONS[w][0] - 1);
                    normals[v][1] -= a * (2 * VERTICES_POSITIONS[w][1] - 1);
                    normals[v][2] -= a * (2 * VERTICES_POSITIONS[w][2] - 1);
                }
                else {
                    n.x -= a * (2 * VERTICES_POSITIONS[w][0] - 1);
                    n.y -= a * (2 * VERTICES_POSITIONS[w][1] - 1);
                    n.z -= a * (2 * VERTICES_POSITIONS[w][2] - 1);
                }
            }
            densities[v] /= k;
        }
        if (sum_a == 0) continue;
        nb_tri = mc_compute(densities, tri);
        if (effects & EFFECT_FLAT) vec3_normalize(&n);

        for (i = 0; i < nb_tri; i++) {
            for (v = 0; v < 3; v++) {
                vi = (nb_tri_tot + i) * 3 + v;
                memcpy(out[vi].color, color, sizeof(color));
                out[vi].color[3] = 255;
                mc_interp_pos(&tri[i][v], out[vi].pos);
                if (!(effects & EFFECT_FLAT))
                    n = mc_interp_normal(&tri[i][v], normals);
                out[vi].normal[0] = n.x * 126;
                out[vi].normal[1] = n.y * 126;
                out[vi].normal[2] = n.z * 126;
                out[vi].pos[0] += x * MC_VOXEL_SUB_POS;
                out[vi].pos[1] += y * MC_VOXEL_SUB_POS;
                out[vi].pos[2] += z * MC_VOXEL_SUB_POS;
                // XXX: this shouldn't matter.
                memset(out[vi].bshadow_uv, 0, sizeof(out[vi].bshadow_uv));
                memset(out[vi].bump_uv, 0, sizeof(out[vi].bump_uv));
            }
        }
        nb_tri_tot += nb_tri;
    }
    return nb_tri_tot;
}

